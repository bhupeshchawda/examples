package com.example.dedup;

public class POJO
{
  private String id;
  private long time;
  private String label;

  public String getId()
  {
    return id;
  }
  public void setId(String id)
  {
    this.id = id;
  }
  public long getTime()
  {
    return time;
  }
  public void setTime(long time)
  {
    this.time = time;
  }
  public String getLabel()
  {
    return label;
  }
  public void setLabel(String label)
  {
    this.label = label;
  }

  
}
